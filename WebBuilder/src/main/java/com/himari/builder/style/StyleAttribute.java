package com.himari.builder.style;

import java.util.Objects;

public class StyleAttribute<T> {

    private String name;
    private T value;

    public StyleAttribute(String name, T value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public T getValue() {
        return value;
    }

    public void setValue(T value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object object) {
        if (this == object) {
            return true;
        }

        if (object == null || getClass() != object.getClass()) {
            return false;
        }

        StyleAttribute<?> flag = (StyleAttribute<?>) object;

        return Objects.equals(name, flag.name);
    }

    @Override
    public int hashCode() {
        return name != null ? name.hashCode() : 0;
    }
}